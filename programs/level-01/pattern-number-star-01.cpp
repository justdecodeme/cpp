#include <iostream>
using namespace std;

int main() 
{
    int n, r = 1, c;
    int x = -2;

    cin >> n;

    while(r <= n) {
        c = 1;
        while(c <= n-r+1) {
            cout << c;
            c++;
        }

        x += 2;

        c = 1;
        while(c <= x) {
            cout << "*";
            c++;
        }

        c = 1;
        while(c <= n-r+1) {
            cout << n-r+2-c;
            c++;
        }
        
        cout << endl;
        r++;
    }
}